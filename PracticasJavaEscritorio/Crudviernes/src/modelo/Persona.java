
package modelo;

public class Persona {
    int ced;
    String nom;
    String ape;
    float sue;
    
    public Persona()
    {
    
    }

    public int getCed() {
        return ced;
    }

    public void setCed(int ced) {
        this.ced = ced;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getApe() {
        return ape;
    }

    public void setApe(String ape) {
        this.ape = ape;
    }

    public float getSue() {
        return sue;
    }

    public void setSue(float sue) {
        this.sue = sue;
    }
    
    
    
}
